#include "ft_printf.h"

int	ft_print_s(char *s)
{
	int	i;

	i = 0;
	if (!s)
		s = "(null)";
	while (s[i])
	{
		if (ft_print_c(s[i]) == -1)
			return (-1);
		i++;
	}
	return (i);
}
